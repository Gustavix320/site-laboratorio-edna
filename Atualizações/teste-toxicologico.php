        <p><strong class="title">Como funciona o Exame:</strong></p>
        <p>O Exame do Cabelo é a única tecnologia capaz de detectar o uso constante de substâncias psicoativas com uma visão retroativa mínima de 90 dias. O teste identifica hábitos e costumes em relação ao eventual consumo de drogas.</p>
        <p>Através do Exame do Cabelo, uma grande variedade de substâncias pode ser identificada, incluindo, mas não limitando-se, as anfetaminas, cocaína, maconha e opiáceos.</p>
        <p>A análise bioquímica é feita através de uma pequena amostra de cabelos ou pelos do corpo. Esse material é suficiente para a realização do teste. O procedimento da coleta não afeta a estética do motorista e é indolor, não invasivo e não contagioso.</p>
        <p>O prazo estimado para obter o resultado do exame do cabelo varia entre 04 e 10 dias, nas Regiões Sul e Sudeste, que respondem por mais de 70% das CNHs das categorias C, D e E no Brasil. Motoristas das outras regiões poderão checar o prazo para a entrega de seus exames na tabela ao lado.</p>
        <p class="titulos-paginas">PRINCIPAIS DÚVIDAS:</p>
        <p><strong class="title">O exame testa álcool?</strong><br>
            Não será testado concentração de álcool no cabelo.</p>
        <p><strong class="title">Quanto tempo acusa o uso da droga?</strong><br>
            O exame faz uma análise retroativa de 90 dias, podendo se estender por até 180 dias.</p>
        <p><strong class="title">Como é o processo de coleta?</strong><br>
            Serão coletadas pequenas amostras de cabelos ou pelos do corpo. Caso você não tenha o mínimo de 4 centímetros de cabelo da raiz até a ponta, será necessário que a coleta seja de pelos do corpo.</p>
        <p>Em caso de pouco pelo/cabelo, posso fazer exame de sangue/ urina/ saliva?<br>
            Não. Caso você seja portador de alguma doença que afete o crescimento de cabelos ou pelos pelo corpo, como Alopécia Universal (doença dermatológica), poderá solicitar, mediante comprovação médica, que o seu exame seja feito a partir de amostras das suas unhas.</p>
        <p><strong class="title">É necessário agendar o meu exame?</strong><br>
            Não há necessidade. Apenas se dirigir a Unidade Cerqueira Cezar.</p>
        <p><strong class="title">Quais são os documentos necessários para realizar coleta?</strong><br>
            Compareça ao ponto de coleta escolhido portando um documento de identificação com foto e seu CPF.</p>
        <p><strong class="title">Qual o prazo de validade do laudo?</strong><br>
            O laudo tem validade de 60 dias a partir da data da coleta.</p>
        <p><strong class="title">Se eu incluir categoria de moto (A), preciso realizar o exame?</strong><br>
            Sim, a adição de categoria (A) necessita do exame, pois é considerada como uma renovação da sua carteira C, D ou E.</p>
