        <label class="title-medium">Orientações quanto ao procedimentos dos exames</label>
        <p>O procedimento de coleta de sangue é um processo simples e tranqüilo, porém pode provocar extravasamento sanguíneo sob a pele, dor no local da punção, porém suportável e raramente desmaios em pessoas sensíveis. Para evitar a formação de hematomas é indispensável comprimir o local da punção por 5 minutos. Durante um período de 4 horas, o braço onde a coleta foi realizada não deverá carregar peso, nem mesmo uma bolsa.</p>
        <p>Os materiais utilizados, como: seringas, agulhas e luvas, são totalmente descartáveis e de uso individual. Não há qualquer risco de contaminação. Nossos profissionais são habilitados e perfeitamente treinados para o atendimento. Os tubos e frascos de coleta são etiquetados com códigos de barras na presença do paciente, para sua maior segurança.</p>
        <p>Eventuais exigências técnicas, para segurança dos resultados, poderão acarretar uma nova coleta (sem ônus) ou alterações na data de entrega, que serão previamente comunicadas.</p>
        <p>Os exames laboratoriais são realizados por metodologias referendadas e controladas através de Programas da Qualidade internos e externos. Entretanto, condições decorrentes do estado do paciente, das próprias metodologias e reagentes, bem como da execução, poderão ocasionar alterações, devendo os resultados ser avaliados e correlacionados a parâmetros clínicos do paciente com seus medicamentos e interações. Em possíveis casos de dúvidas, solicitar diretamente, ou através de seu médico a confirmação do resultado.</p>     
