    <div class="container">
        <p>O teste de paternidade, também chamado de teste de DNA, permite comparar as informações genéticas do DNA da criança com aquelas encontradas no DNA do suposto pai. Basta uma pequena amostra de sangue dos envolvidos (suposto pai, mãe e filho).</p>
        <p>Para coletas em que mãe e suposto pai são menores de idade: é obrigatória a autorização de um dos pais ou de um responsável legal, bem como o envio de copia da documentação deste responsável.</p>
        <p><strong class="title">Como é feito o exame?</strong></p>
        <p>O exame é feito pela análise de sangue colhido em laboratório.</p>
        <ul>
            <li>É necessário que ambas as partes presenciem a coleta, fazendo o exame no mesmo local e na mesma hora.</li>
            <li>Não é preciso estar em jejum, pois a ingestão de alimentos, não prejudica a análise.</li>
            <li>Mesmo que um dos envolvidos esteja doente ou sob tratamento médico, pode fazer o teste.</li>
            <li>No momento da coleta de sangue, a identidade das pessoas é comprovada por meio de documentos, com foto e, no caso dos menores, pela certidão de nascimento.</li>
        </ul>
        <p><strong class="title">O teste pode ser feito sem participação da mãe e após a morte do suposto pai?</strong></p>
        <p>Sim, entretanto, sem a mãe, o teste é mais complexo, pois requer a análise de um número maior de genes. Quando o pai já faleceu, as características genéticas podem ser obtidas através do exame de membros da família até o segundo e terceiro graus e, também através da exumação do cadáver.</p>
        <p><strong class="title">É preciso ordem médica ou judicial para realizar o teste?</strong></p>
        <p>Não. A maioria dos casos é resolvida extrajudicialmente. O resultado do exame de paternidade é estritamente confidencial. É fornecido somente a quem contratou o teste ou às pessoas por ele (a) indicadas. Em casos judiciais, o laudo é enviado diretamente ao juiz.</p>
